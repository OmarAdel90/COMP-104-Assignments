#include <iostream>
using namespace std;

int main(){
// \n Prints new line character
cout<<"Hello world\n"<<endl;

// \t Tab chracter (8 spaces)
cout<<"Hello\t world"<<endl;

// \v Vertical tab
cout<<"Hello\v world"<<endl;

// \b Backspace (removes a letter before it)
cout<<"Hello\b world"<<endl;

// \a Alerts user witha beeping sound
cout<<"Hello world \a"<<endl;

return 0;   

}
